#----------------------------------------------------------------------------------------------------------------
#                                                                                                       Variables
#----------------------------------------------------------------------------------------------------------------
URLZIMBRA="10.0.0.1"
FOLDERIMPORT="/opt/zimbra/migracion"
FOLDERLIST="/opt/zimbra/scripts"
LISTADO="ListadoCuentas.txt"


#----------------------------------------------------------------------------------------------------------------
#                                                                                                       Funciones
#----------------------------------------------------------------------------------------------------------------
for CUENTA in `cat $FOLDERLIST/$LISTADO | cut -d ";" -f 1`; do
 zmmailbox -z -m $CUENTA -t 0 postRestURL -u https://$URLZIMBRA:7071 "//?fmt=tgz&resolve=skip" $FOLDERIMPORT/$CUENTA.tgz
 echo "$CUENTA -- finished"
done
